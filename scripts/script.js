function showcollapse(){
    document.getElementById("collapsediv").style.display="block";
    document.getElementById("menuimg").style.display="none";
    document.getElementById("dropimg").style.display="block";
}

function hidecollapse(){
    document.getElementById("collapsediv").style.display="none";
    document.getElementById("menuimg").style.display="block";
    document.getElementById("dropimg").style.display="none";
}